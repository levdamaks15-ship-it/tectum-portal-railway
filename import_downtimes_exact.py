def get_category(breakdown_name):
    b_lower = breakdown_name.lower()
    if "санитарный день" in b_lower:
        return "Санитарный день"
    if any(x in b_lower for x in ["ппр", "комплексное обслуживание", "то и ппр"]):
        return "ТО и ППР"
    if any(x in b_lower for x in ["двигател", "катушк", "реле", "датчик", "электрод", "кип", "кабель", "воздух", "нет воздуха", "нет пара", "пар", "электричес", "привод", "компрессор", "осушитель", "пускатель"]):
        return "Энергетические"
